public class RelationalOperators {
    public static void main(String args[]){

        // == Equal to
        // != Not Equal to
        // > Greater than
        // < Less than
        // <= Less than equal to
        // >= Greater than equal to

        
        int a = 13;
        int b = 11;

        boolean result = a > b;
    
        System.out.println(result);

    }
}
